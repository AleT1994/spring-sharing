package com.caronte.sharing.entities;

public class GraficoVeicolo {

	private String tipoVeicolo;
	private int sommaTipo;
	
	public String getTipoVeicolo() {
		return tipoVeicolo;
	}
	public void setTipoVeicolo(String tipoVeicolo) {
		this.tipoVeicolo = tipoVeicolo;
	}
	public int getSommaTipo() {
		return sommaTipo;
	}
	public void setSommaTipo(int sommaTipo) {
		this.sommaTipo = sommaTipo;
	}
	
}
